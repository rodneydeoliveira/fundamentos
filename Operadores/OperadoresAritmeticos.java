package fundamentos.Operadores;

public class OperadoresAritmeticos {
	public static void main(String[] args) {
		
		System.out.println(2+3);
		
	var a = 33.45;
	double b = 12.32;
	System.out.println(a+b);
	int c = 8;
	int d = 3;
	System.out.println(c+d);
	System.out.println(c-d);
	System.out.println(c*d);
	System.out.println(c/d); // divisão entre números inteiros, o resultado será um inteiro
	System.out.println(c/(double) d); // cast para double
	
	System.out.println(c%d); // resto da divisão
	}
}
